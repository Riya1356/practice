public class Swapping {
    public static void main(String[] args) {



        int a=10;
        int b=20;
        System.out.println("Before Swapping:-");
        System.out.println(a);
        System.out.println(b);
        int temp;
        temp=a;
        a=b;
        b=temp;
        System.out.println("After Swapping the values:");
        System.out.println(a);
        System.out.println(b);

    }
}
